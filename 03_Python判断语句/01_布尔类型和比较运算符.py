"""
演示布尔类型的定义
以及比较运算符的运用
"""
# 定义变量存储布尔类型的数据
# 比较运算符的使用
# ==,!=,>,<,>=,<=
# 演示进行内容的相等比较
num1 = 10
num2 = 15
print(f"{num1 != num2}")

name1 = "zpc"
name2 = "zp"
print(f"{name1 == name2}")

# 演示大于小于，大于等于小于等于的比较运算
num1 = 10
num2 = 10
print(f"{num1 >= num2}")
print(f"{num1 <= num2}")
num2 = 11
print(f"{num1 >= num2}")
print(f"{num1 <= num2}")

